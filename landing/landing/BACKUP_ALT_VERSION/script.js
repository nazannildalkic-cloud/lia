function initLangAndUI() {
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', function () {
            const lang = this.getAttribute('data-lang') || this.textContent.trim().toLowerCase();
            if (window.setLanguage) window.setLanguage(lang);
        });
    });

    const savedLang = localStorage.getItem('selectedLang') || document.documentElement.lang || 'de';
    if (window.setLanguage) window.setLanguage(savedLang);
}

